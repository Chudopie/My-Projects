const dia = 2; //Domingo

switch (
  dia //evalua con triple igual el valor y tipo de dato
) {
  case 0:
    console.log("Domingo");
    break;
  case 1:
    console.log("Lunes");
    break;
  case 2:
    console.log("Martes");
    break;
  case 3:
    console.log("Miercoles");
    break;
  case 4:
    console.log("Jueves");
    break;
  default:
    console.log("No es viernes ni sabado");
}
